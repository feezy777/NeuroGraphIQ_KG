export function buildOverviewCards(snapshot) {
  return [
    { key: "ontology", label: "Ontology", value: snapshot.ontologyCount || 0 },
    { key: "files", label: "Files", value: snapshot.fileCount || 0 },
    { key: "jobs", label: "Extraction Jobs", value: snapshot.jobCount || 0 },
    { key: "entities", label: "Entities", value: snapshot.entityCount || 0 },
    { key: "relations", label: "Relations", value: snapshot.relationCount || 0 },
    { key: "circuits", label: "Circuits", value: snapshot.circuitCount || 0 },
  ];
}

function zhBlock(item) {
  return [
    `动作: ${item.title}`,
    `用途: ${item.purpose}`,
    `前置条件: ${item.preconditions}`,
    `输入要求: ${item.inputs}`,
    `风险: ${item.risks}`,
    `下一步: ${item.next}`,
  ].join("\n");
}

function enBlock(item) {
  return [
    `Action: ${item.titleEn || item.title}`,
    `Purpose: ${item.purposeEn || item.purpose}`,
    `Preconditions: ${item.preconditionsEn || item.preconditions}`,
    `Inputs: ${item.inputsEn || item.inputs}`,
    `Risks: ${item.risksEn || item.risks}`,
    `Next: ${item.nextEn || item.next}`,
  ].join("\n");
}

